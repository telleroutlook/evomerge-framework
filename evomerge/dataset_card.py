"""evomerge.dataset_card — generate a DATASET_CARD.md from an ExportManifest.

This module contains the core generation logic shared between:
  - run_export() (called automatically on every export)
  - scripts/generate-dataset-card.py (standalone CLI / CI check)
"""
from __future__ import annotations

import time
from dataclasses import dataclass, field
from pathlib import Path

REPO_ROOT = Path(__file__).parent.parent
_TEMPLATE_PATH = REPO_ROOT / "docs" / "dataset-card-template.md"

# Fallback minimal template used when docs/dataset-card-template.md is absent
# (e.g. the package was installed from a wheel that doesn't include docs/).
_FALLBACK_TEMPLATE = """\
# Dataset Card — {{DATASET_NAME}}

Generated by evomerge {{VERSION}} on {{DATE}}.

## Record counts

| Split | Records |
|---|---|
| SFT | {{N_SFT}} |
| DPO | {{N_DPO}} |
| PPO | {{N_PPO}} |
| Compliance SFT | {{N_COMPLIANCE_SFT}} |
| Compliance DPO | {{N_COMPLIANCE_DPO}} |
| Router | {{N_ROUTER}} |
| **Total** | **{{N_TOTAL}}** |

## Quality

- Invalid records: {{N_INVALID}}
- Contaminated records: {{N_CONTAMINATED}} ({{CONTAMINATION_RATE}}, threshold {{CONTAMINATION_THRESHOLD}})

## Provenance

- Source: [wasmagent-js](https://github.com/WasmAgent/wasmagent-js) + [bscode](https://github.com/WasmAgent/bscode)
- Pipeline: [trace-pipeline](https://github.com/WasmAgent/trace-pipeline)
- License: {{LICENSE}}
"""


def generate_dataset_card(manifest: dict, *, name: str, date: str = "") -> str:
    """Render a DATASET_CARD.md string from an ExportManifest dict.

    Args:
        manifest: dict as returned by ExportManifest.to_dict().
        name: human-readable dataset name, e.g. "wasmagent-compliance-v1".
        date: ISO date string YYYY-MM-DD. Defaults to today.

    Returns:
        Rendered Markdown string.
    """
    if not date:
        date = time.strftime("%Y-%m-%d")

    template = (
        _TEMPLATE_PATH.read_text()
        if _TEMPLATE_PATH.exists()
        else _FALLBACK_TEMPLATE
    )

    n_total = (
        manifest.get("n_sft", 0)
        + manifest.get("n_dpo", 0)
        + manifest.get("n_ppo", 0)
        + manifest.get("n_compliance_sft", 0)
        + manifest.get("n_compliance_dpo", 0)
        + manifest.get("n_router", 0)
    )
    n_contaminated = manifest.get("n_contaminated", 0)
    contamination_rate = f"{n_contaminated / n_total:.1%}" if n_total else "N/A"

    replacements = {
        "{{DATASET_NAME}}": name,
        "{{VERSION}}": "1.0.0",
        "{{DATE}}": date,
        "{{LICENSE}}": "Apache-2.0",
        "{{N_SFT}}": str(manifest.get("n_sft", 0)),
        "{{N_DPO}}": str(manifest.get("n_dpo", 0)),
        "{{N_PPO}}": str(manifest.get("n_ppo", 0)),
        "{{N_COMPLIANCE_SFT}}": str(manifest.get("n_compliance_sft", 0)),
        "{{N_COMPLIANCE_DPO}}": str(manifest.get("n_compliance_dpo", 0)),
        "{{N_ROUTER}}": str(manifest.get("n_router", 0)),
        "{{N_INVALID}}": str(manifest.get("n_invalid", 0)),
        "{{N_CONTAMINATED}}": str(n_contaminated),
        "{{N_TOTAL}}": str(n_total),
        "{{CONTAMINATION_RATE}}": contamination_rate,
        "{{CONTAMINATION_THRESHOLD}}": "0.2",
        "{{TASK_TYPE}}": "instruction-following / code editing",
        "{{ROLLOUT_MODELS}}": "see rollout metadata",
        "{{KERNELS}}": "QuickJS WASM",
        "{{COLLECTION_PERIOD}}": date,
        "{{EVAL_SET}}": "IFEval-50 (google/IFEval stratified subset)",
        "{{SEED_1}}": "42", "{{SEED_1_N}}": "—", "{{SEED_1_RATE}}": "—",
        "{{SEED_2}}": "43", "{{SEED_2_N}}": "—", "{{SEED_2_RATE}}": "—",
        "{{ROLLOUT_JSONL_PATH}}": "data/rollouts.jsonl",
        "{{OUT_DIR}}": "data/training/",
        "{{SEED}}": "42",
        "{{LIMITATION_1}}": "Small model (≤3B params) rollouts only; larger model behaviour untested",
        "{{LIMITATION_2}}": "IFEval task distribution — not representative of open-domain tasks",
    }

    card = template
    for placeholder, value in replacements.items():
        card = card.replace(placeholder, value)
    return card


__all__ = ["generate_dataset_card", "TraceCard", "generate_trace_card"]


# ── Trace Card ────────────────────────────────────────────────────────────────

@dataclass
class TraceCard:
    """Structured metadata card for a trace dataset.

    Matches the P1-4 TraceCard schema from the reform roadmap.
    """
    dataset_name: str
    source_repo: str
    source_commit: str
    collection_mode: str              # "demo" | "evidence" | "training"
    data_subject_risk: str            # "low" | "medium" | "high"
    pii_redaction_profile: str
    license: str
    consent_basis: str
    model_provider: str
    model_id: str
    policy_bundle: str
    verifier_bundle: str
    known_biases: list[str] = field(default_factory=list)
    known_contamination_risks: list[str] = field(default_factory=list)
    excluded_fields: list[str] = field(default_factory=list)
    replay_command: str = ""
    aep_schema_version: str = "aep/v0.1"
    run_receipt_path: str = ""


def generate_trace_card(card: TraceCard, *, date: str = "") -> str:
    """Render a TRACE_CARD.md string from a TraceCard."""
    import time
    if not date:
        date = time.strftime("%Y-%m-%d")

    biases = "\n".join(f"- {b}" for b in card.known_biases) or "- None reported"
    contamination = "\n".join(f"- {c}" for c in card.known_contamination_risks) or "- None reported"
    excluded = ", ".join(card.excluded_fields) or "none"

    return f"""# Trace Card — {card.dataset_name}

> Generated {date}

## Collection

| Field | Value |
|---|---|
| Source repo | {card.source_repo} |
| Source commit | {card.source_commit} |
| Collection mode | {card.collection_mode} |
| Data subject risk | {card.data_subject_risk} |
| PII redaction profile | {card.pii_redaction_profile} |
| Consent basis | {card.consent_basis} |

## Model & Policy

| Field | Value |
|---|---|
| Model provider | {card.model_provider} |
| Model ID | {card.model_id} |
| Policy bundle | {card.policy_bundle} |
| Verifier bundle | {card.verifier_bundle} |
| AEP schema version | {card.aep_schema_version} |

## Provenance

- Run receipt: {card.run_receipt_path or "not generated"}
- Replay: `{card.replay_command}`

## License

{card.license} · {card.consent_basis}

## Known biases

{biases}

## Known contamination risks

{contamination}

## Excluded fields

{excluded}
"""
